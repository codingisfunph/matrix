package edu.codingisfunph.controls;

public class SwitchRows{

      public SwitchRows( int row1, int row2){
          this.row1 = row1;
          this.row2 = row2;
      }

      public int getRow1(){ return row1; }
      public int getRow2(){ return row2; }

      private int row1;
      private int row2;
}
